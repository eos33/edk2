    parse_cmdline(ImageHandle);

    AOPrintMessage("\n");
    gBS->Stall(1000);
    AOSerialPortRead(Buffer, sizeof(Buffer));    
    debug_print("str=%a",Buffer);
    debug_pause();

    gBS->Stall(50000);
    
    AOPrintMessage("#STOP WDT\r\n");
    gBS->Stall(1000);
